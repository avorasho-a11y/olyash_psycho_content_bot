async def saturday_reminder():
    """Каждую субботу в 10:00 МСК спрашиваем какой курс"""
    while True:
        now = datetime.now(MOSCOW_TZ)
        # Суббота = 5 (weekday), 10:00
        if now.weekday() == 5 and now.hour == 10 and now.minute == 0:
            try:
                await bot.send_message(
                    ADMIN_ID,
                    "привет! какой курс берём на эту неделю? напиши название и я в воскресенье пришлю готовый контент-план."
                )
                logger.info("Sent Saturday reminder")
            except Exception as e:
                logger.error(f"Saturday reminder error: {e}")
            await asyncio.sleep(60)  # ждём минуту чтобы не отправить дважды
        await asyncio.sleep(30)

async def main():
    asyncio.create_task(saturday_reminder())
    await dp.start_polling(bot)

if __name__ == "__main__":
    asyncio.run(main())
