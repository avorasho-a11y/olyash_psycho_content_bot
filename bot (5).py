@dp.message(Command("plan"))
async def cmd_plan(message: types.Message, state: FSMContext):
    if message.from_user.id != ADMIN_ID:
        return
    await message.answer("какой курс берём на эту неделю?\n\nнапример: тревожная привязанность, токсичные отношения")
    await state.set_state(ContentPlan.waiting_for_course)

@dp.message(ContentPlan.waiting_for_course)
async def process_course_name(message: types.Message, state: FSMContext):
    course_name = message.text.strip()
    await state.set_state(ContentPlan.generating)
    
    status_msg = await message.answer(f"беру курс «{course_name}», анализирую тренды и генерирую план на неделю. это займёт минуту-две...")
    
    try:
        # Анализ трендов
        trends = search_trends_via_claude(course_name)
        
        await bot.edit_message_text(
            "тренды нашла, теперь пишу контент на 7 дней...",
            chat_id=message.chat.id,
            message_id=status_msg.message_id
        )
        
        # Генерация контента
        content = generate_week_content(course_name, trends)
        
        await bot.delete_message(chat_id=message.chat.id, message_id=status_msg.message_id)
        
        # Отправляем заголовок
        week_num = datetime.now(MOSCOW_TZ).isocalendar()[1]
        await message.answer(
            f"📋 контент-план на неделю {week_num}\nкурс: {course_name}\n\n"
            f"дальше идут 7 дней — по одному сообщению на каждый день."
        )
        
        # Разбиваем по дням и отправляем
        days = []
        current_day = []
        for line in content.split("\n"):
            if line.startswith("ДЕНЬ ") and current_day:
                days.append("\n".join(current_day))
                current_day = [line]
            else:
                current_day.append(line)
        if current_day:
            days.append("\n".join(current_day))
        
        if len(days) <= 1:
            # Если не разбилось по дням — отправляем кусками
            parts = split_long_message(content)
            for i, part in enumerate(parts, 1):
                await message.answer(f"часть {i}/{len(parts)}:\n\n{part}")
                await asyncio.sleep(0.5)
        else:
            for i, day_content in enumerate(days, 1):
                parts = split_long_message(day_content)
                for part in parts:
                    await message.answer(part)
                    await asyncio.sleep(0.3)
        
        await message.answer("всё готово! план на неделю отправлен.")
        
    except Exception as e:
        logger.error(f"Generation error: {e}")
        await message.answer(f"что-то пошло не так: {e}\n\nпопробуй ещё раз /plan")
    
    await state.clear()

