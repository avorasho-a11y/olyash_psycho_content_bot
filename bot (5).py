def split_long_message(text: str, max_len: int = 4000) -> list[str]:
    """Разбиваем длинный текст на части для Telegram"""
    if len(text) <= max_len:
        return [text]
    
    parts = []
    while text:
        if len(text) <= max_len:
            parts.append(text)
            break
        # Режем по разделителю ДЕНЬ или по абзацу
        cut = text.rfind("\n\nДЕНЬ ", 0, max_len)
        if cut == -1:
            cut = text.rfind("\n\n", 0, max_len)
        if cut == -1:
            cut = max_len
        parts.append(text[:cut])
        text = text[cut:].lstrip()
    
    return parts

